							<div class="sub-section" id="hd_2">
							 	<h3  style="padding:40px 0px 0px 0px;">单色搭配（monochromatic）</h3>
								<p>这个不用解释，就是只用一种颜色的搭配。</p>
								<img src="style/images/dansedapei.png"/>
								</br>
							</div>
							</br>
							<hr/>
							<div class="sub-section" id="hd_3">
							 	<h3  style="padding:40px 0px 0px 0px;">类似色搭配（analogous colors）</h3>
								<p>色环上任意三个相邻的颜色，被称为类似色。通常认为，它们放在一起会很和谐。</p>
								<img src="style/images/neisise.png"/>
								</br>

								<p>类似色是选择相近颜色时十分不错的方法，可以在同一个色调中制造丰富的质感和层次。理所当然成为配色法则中最经济适用的一条。</p>
								<img src="style/images/neisise1.jpg"/>
								</br>

								<p>一大片近似色将梵高叔叔的忧郁高冷展现的淋漓尽致。</p>
								<img src="style/images/neisise2.jpg"/>
								</br>

								<p>再放眼时尚圈，同类色也是除了黑白灰以外最安全的穿搭法则哦！</p>
								<img src="style/images/neisise3.jpg"/>
								</br>

								<p>在选择的时候要注意比例上的调和，选一个为主要颜色。</p>
								<img src="style/images/neisise4.jpg"/>
								</br>

							</div>
							</br>
							<hr/>
							<div class="sub-section" id="hd_4">
							 	<h3  style="padding:40px 0px 0px 0px;">补色搭配（complementary colors）</h3>
								<p>色环上相对的两种颜色，被称为互补色。放在一起，会形成对比效果。比如蓝色和橙色、红色和绿色、黄色和紫色等。</p>
								<img src="style/images/busedapei.png"/>

								<p>互补色有非常强烈的对比度，在颜色饱和度很高的情况下，可以创建很多十分震撼的视觉效果。很多广告和招贴为了吸引眼球经常会使用，用作强调提示作用效果是极好的。但是记得，在文本中使用互补色是很糟糕的。</p>
								<img src="style/images/hubusedapei2.jpg"/>
								</br>
								<p>要用好互补色，最好的办法就是：改变明度、降低纯度，看起来会柔和许多。下面看几个例子。</p>
								<img src="style/images/zuihaodehubuse.jpg"/>
								<p>降低纯度后的互补色相当和谐。</p>
								<img src="style/images/jiangdichunduhouhubuse.jpg"/>
								<p>《天使爱美丽》中的这个场景，是一个典型的红绿互补色。室内装修中互补色也很管用，清新又不失活泼。</p>
								<img src="style/images/tianshiaimeili.jpg"/>
							</div>
							</br>
							<hr/>
							<div class="sub-section" id="hd_5">
							 	<h3  style="padding:40px 0px 0px 0px;">分散互补色搭配（split-complementary colors）</h3>
								<img src="style/images/fensanhubuse.png"/>
								<p>寻找三种颜色，其中两种互相类似，另一种与它们形成对比，这就是分散互补色搭配。分散互补色搭配是初学者很好的选择，怎么搭都很好看。</p>
								<img src="style/images/fshbs2.jpg"/>
								<p>红、深蓝与浅蓝搭配，完美突出对比感，画面又不失和谐。</p>
							</div>
							</br>
