							<div class="sub-section" id="hue_5">
							 	<h3  style="padding:40px 0px 0px 0px;">色彩的象征性</h3>
								<br/>
							 	<p class="p_suojin">与大部分人的经验与联想有关，人们通过与自然界和社会的接触，逐步形成色的概念和联想。色彩的象征意义是具有世界性的，不同的民族产生的差异不大。</p>
							 	</br>
								<h2 class="example">红色</h2><p class="p_suojin">最引人注目的色彩，具有强烈的感染力，它是火的色、血的色。象征热情、喜庆、幸福。另一方面又象征警觉、危险。红色色感刺激强烈，在色彩配合中常起着主色和重要的调和对比作用，是使用得最多的色。</p>
								</br>
								<h2 class="example">黄色</h2><p class="p_suojin">是阳光的色彩，象征光明、希望、高贵、愉快。浅黄色表示柔弱，灰黄色表示病态。黄色在纯色中明度最高，与红色色系的色配合产生辉煌华丽、热烈喜庆的效果，与兰色色系的色配合产生淡雅宁静、柔和清爽的效果。</p>
								</br>
								<h2 class="example">蓝色</h2><p class="p_suojin">是天空的色彩，象征和平、安静、纯洁、理智。另一方面又有消极、冷淡、保守等意味。蓝色与红、黄等色运用得当，能构成和谐的对比调和关系。</p>
								</br>
								<h2 class="example">绿色</h2><p class="p_suojin">是植物是色彩，象征着平静与安全，带灰褐绿的色则象征着衰老和终止。绿色和蓝色配合显得柔和宁静，和黄色配合显得明快清新。由于绿色的视认性不高，多为陪衬的中型色彩运用。</p>
								</br>
								<h2 class="example">橙色</h2><p class="p_suojin">秋天收获的颜色，鲜艳的橙色比红色更为温暖、华美，是所有色彩中最温暖的色彩。橙色象征快乐、健康、勇敢。</p>
								</br>
								<h2 class="example">紫色</h2><p class="p_suojin">象征优美、高贵、尊严，另一方面又有孤独、神秘等意味。淡紫色有高雅和魔力的感觉，深紫色则有沉重、庄严的感觉。与红色配合显得华丽和谐，与蓝色配合显得华贵低沉，与绿色配合显得热情成熟。运用得当能构成新颖别致的效果。</p>
								</br>
								<h2 class="example">白色</h2><p class="p_suojin">表示纯粹与洁白的色，象征纯洁、朴素、高雅等。作为非彩色的极色，白色与黑色一样，与所有的色彩构成明快的对比调和关系，与黑色相配，构成简洁明确、朴素有力的效果，给人一种重量感和稳定感，有很好的视觉传达能力。 </p>
								</br>
								<h2 class="example">黑色</h2><p class="p_suojin">是暗色，是明度最低的非彩色，象征着力量，有时又意味着不吉祥和罪恶。能和许多色彩构成良好的对比调和关系，运用范围很广。</p>

