							<div class="sub-section" id="hd_6">
							 	<h3  style="padding:40px 0px 0px 0px;">三角对立配色（Triadic colors）</h3>
								<img src="style/images/sjdlps.png"/>
								<p>此外，如果要寻找三种互相平衡的颜色，可以选择12色环上任意三个三角对立的颜色。</p>
								<img src="style/images/sjdlps2.jpg"/>
								<p>要成功地使用三色系产生和谐效果，需要谨慎地在色彩间取得平衡——只用一种主色，其他两种颜色作烘托。</p>
