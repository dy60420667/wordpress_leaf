							<div class="sub-section" id="hue_1">
								<h3  style="padding:40px 0px 0px 0px;">色彩的相关名词解释</h3>
								<img src="style/images/sanyuanse.png"  style="float:right" />
								<h2 class="example">三原色</h2>
								<p>红黄蓝（色彩的最基本单位）</p>
								<br/>
								<h2 class="example">二次色</h2>
								<p>橙绿紫</p>
								<p>红黄蓝互相混合后分别产生橙绿紫三个二次色（三色等量混合后产生黑色）</p>
								<br/>
								<h2 class="example">三次色</h2>
								<p>红橙 黄橙 黄绿 蓝绿 蓝紫 红紫</p>
								<p>三次色是由是由原色和二次色混合后形成的色彩</p>
								<br/>

								<h2 class="example">色相</h2>
								<p>反应色彩相貌的名称，如红橙黄绿青蓝紫</p>
								<img src="style/images/sexianghuan.png"/>
								<br/>

								<h2 class="example">明度</h2>
								<p>指色彩的明亮程度</p>
								<img src="style/images/mingdu.png"/>
								<br/>

								<h2 class="example">纯度</h2>
								<p>纯度最高的色彩就是三原色（三原色纯度最高，二次色次之，三次色纯度又低了，混合的色彩越高，纯度越低），随着纯度的降低，就会变化为暗淡的，没有色相的色彩。纯度降到最低就会失去色相，变为无彩色。</p>
								<img src="style/images/chundu.png"/>
								<br/>
							</div>
							</br>
							<hr/>
							<div class="sub-section" id="hue_2">
								<h3  style="padding:40px 0px 0px 0px;">色彩的纯度</h3>
								<p>纯度：纯度最高的色彩就是原色（三原色纯度最高、二次色次之，三次色纯度又低了，混合的色彩越多，纯度越低），随着纯度的降低，就会变化为暗淡的，没有色相的色彩。纯度降到最低就是失去色相，变为无彩色。 </p>
								<br/>
								<h2 class="example">纯度对色彩的影响</h2>
								<p class="p_suojin">纯度高的色彩显得比较华丽，纯度低的色彩给人一种柔和、雅致的感觉。二者相比而言，高纯度色彩在搭配中较难驾驭，而低纯度色彩比较好驾驭。 </p>
								<br/>
								<h2 class="example">纯度和明度的关系</h2>
								<p class="p_suojin">同一色相的色彩，不掺杂白色或者黑色，则被称为纯色。在纯色中加入不同明度的无彩色，会出现不同的纯度。以蓝色为例，向纯蓝色中加入一点白色，纯度下降而明度上升，变为淡蓝色。同理在纯蓝中，加入一点黑色，纯度下降明度也下降，变成了深蓝色。 </p>
								<br/>
								<h2 class="example">明度对色彩的影响</h2>
								<p class="p_suojin">明度高的会给人一种轻松、明快的感觉，明度低的色彩则会令人产生沉稳、稳重的感觉。在同样体积的情况下，明度高的较轻，有膨胀感，明度低的较重有收缩感。 </p>
							</div>
							</br>
							<hr/>
							<div class="sub-section" id="hue_3">
								<h3  style="padding:40px 0px 0px 0px;">冷色、暖色、中性色</h3>
								<p class="p_suojin">冷色：蓝色、群青、钛青蓝、孔雀蓝、湖蓝、蓝绿色、蓝紫色等等。</p>
								<p class="p_suojin">暖色：红、桔红、粉红、橙色、桔黄、紫红、黄等等。  </p>
								<p class="p_suojin">中性色：黑、白、灰、紫、棕色等等。  </p>
								<br/>
								<p class="p_suojin">冷暖色的作用：暖色系会产生热情、明亮、活泼温暖等感觉。冷色系会令人产生一种沉稳、稳重、消极等感觉。暖色有靠前、膨胀的感觉，冷色有靠后、收缩的感觉。</p>
								<img style="padding:20px" src="style/images/lengsenuansezhongxingse.png"/>
							</div>
							</br>
							<hr/>
							<div class="sub-section" id="hue_4">
								<h3  style="padding:40px 0px 0px 0px;">邻近色、类似色、对比色、互补色</h3>
								<img src="style/images/dapei.png"/>
								<p>色彩的分类很多，在这里指讲我们经常会用到的概念——邻近色、类似色、对比色、互补色。 </p>
								<p>类似色：在色相环上60°以内两种或两种以上的色彩是类似色 </p>
								<p>对比色：在色相环上120°以内两种或两种以上的色彩是对比色 </p>
								<p>互补色：是指在色环上相对180°两个色彩是互补色 </p>
								<p>邻近色：在色相环上30°以内两种或两种以上的色彩是邻近色 </p>
								<br/>
								<img src="style/images/dapeibaibian1.png"/>


