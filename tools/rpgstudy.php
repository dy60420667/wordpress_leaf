							 	<h3  style="padding:40px 0px 0px 0px;">色彩的搭配</h3>

								<p class="p_suojin">关键词：单色 近似色 补色</p>

								<p class="p_suojin">十二色环的存在并不只是一个摆设，它巧妙地建立了配色系统。</p>
								<p class="p_suojin">常见的色彩搭配有：单色搭配，近似色搭配，补色搭配，分散互补色搭配，三角对立的配色搭配……</p>

							</div>
							</br>
